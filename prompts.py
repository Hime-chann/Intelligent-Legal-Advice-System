def get_initial_prompt():
    return "Welcome! Would you like to get legal advice? (yes/no)"

def get_country_prompt():
    return "Please select your country for legal advice."

def get_age_prompt():
    return "Please enter your age."

def get_attorney_type_prompt():
    return "What type of attorney do you need? (e.g., family, criminal, corporate)"

def get_authority_level_prompt():
    return "Where should you go for this issue? (e.g., court, mayor's office, police station)"

def get_query_prompt():
    return "Please describe your legal query in detail."
